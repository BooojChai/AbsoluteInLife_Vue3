            <h3>死角</h3>
                <p>我在OPPO的部长，也是我在三星时的老领导，曾经在我入职三星的新人招待饭局上说过一段让我<b>牢记于心，终身难忘</b>的话：“<b>软件工程师也是<b class="red">手艺人</b>，写代码就是我们吃饭的手艺，无论到哪，手艺不能丢”。</b></p>

                <p>负责开放能力建设，相比于技术能力，更要求沟通能力与协作能力。对照着这段话，这项工作对于<b>精进我的手艺，也就是提升代码能力</b>，并没有什么<b>实质性帮助</b>。有时偶尔停下来回想一下，会惊觉自己已经好久没有坐下来好好写一写代码，思考一下应该用怎样的<b>数据结构</b>，怎么样的<b>设计模式</b>，去编写一段优雅的业务代码，让它具有良好的可维护性和可拓展性。事实上，据我对组内到部门再到对整个软件工程系统的观察，这家公司少有人在意“代码写的咋样”，换句话说，你的手艺咋样，并<b class="red">不重要</b>。</p>

                <p>游戏优化组的业务可以分成两大类——<b class="red">方案与交付</b>。方案是指为运行在OPPO手机上的游戏们定制一些性能、功耗优化的系统方案，做方案对开发人员的要求较高，我之前一直所做的开放能力业务，算是游离在<b>方案的边缘</b>。而交付，是指开发人员需要对一款机型从<b>立项，预研，开发，测试，量产</b>，直到交付到用户手上的过程中出现的所有游戏相关的<b>性能、功耗、发烫问题<b class="red">负责</b>。</b></p>

                <p>如果说做开放能力业务是<b>与“写代码”有些脱钩</b>，那做交付业务，基本上就是在<b>与“计算机与科学技术”这门学科<b class="red">失去关系</b></b>。这么说绝不是空口无凭，是从我的自身经验出发，结合对组内其他做业务业务同事的观察，在充分理解与总结之后<b>提炼</b>出来的。时间临近2020年末时，主管找到我，希望我能承担<b class="red"> Find X2 </b>系列机型的游戏优化交付工作，之前承担这个业务的是我的好朋友，他即将去负责一款全新的机型，而开放能力业务和其他一些零散的游戏方案业务并不能完全填充我的工作时间，我也乐意多学一点，拓展自己的知识边界，于是欣然接下了这项任务。</p>

                <p>我不想用<b>流水账</b>的形式来呈现做游戏交付业务的过程，只想列举一些<b class="red">事实</b>，用于解释什么叫做<b>“与计算机与科学技术这门学科失去关系”</b>：</p>

                <div>
                    <li>在一款机型上市前的开发阶段，测试部门会测试大部分热门游戏，并创建<b>一堆卡顿、发烫之类的Bug</b>，而负责游戏交付业务的工程师需要逐个<b class="red">关闭</b>这些Bug，不解决就无法通过一些所谓的“项目节点”，影响项目进度。而这里存在着一个很明显的<b class="red">悖论</b>：<b>我们要对游戏性能负责，但手机系统里存在许多影响性能的关键模块，其代码掌握在其他项目组手里，我们根本插足不了</b>。于是为了关掉一个Bug，需要和很多其他部门甚至其他系统的项目组<b>Battle</b>，要和测试人员<b>扯皮，争论"<b>这个Bug究竟是不是个Bug"</b></b>。
                        <br>总之，解决的<b>是<b class="red"> Bug </b>，不是问题。</b></li>
                    <li>机型上市之后，用于考核游戏交付业务绩效的核心指标，叫做<a href="https://baike.baidu.com/item/%E5%87%80%E6%8E%A8%E8%8D%90%E5%80%BC/3783368?fromtitle=NPS&fromid=17751327&fr=aladdin" target="_blank"> NPS </a>（用户净推荐值）。用户填写NPS问卷时，如果给了差评，那么负责相应机型交付业务的工程师就要做客服，给那个差评的客户<b>打电话，安抚情绪，加微信，随时准备解答客户的问题，<b class="red">央求</b>下次问卷能不能不打差评</b>，仿佛这家大公司在工程师和用户之间的<b>所有流程，所有角色都不存在</b>一样。我曾经亲耳听到一个工程师同事打电话给客户，开头就说：”你好，我是OPPO的客服...“，实在是<b>好笑又令人心疼</b>。
                        <br>总之，解决的<b>是<b class="red">用户</b>，不是问题。</b></li>
                    <li>大老板有时会在一些<b>社交平台</b>看到用户吐槽游戏体验不好，然后<b class="red">层层施压</b>找到我们，我们就要千方百计联系到那个用户，像处理NPS差评一样服务那个用户，<b>当客服，做陪聊，生怕客户再去社交平台吐槽</b>，然后每天给大老板汇报一下进展，告诉他我们安抚住了用户，用户情绪稳定，方案已在路上之类的。我就曾经被拉进一个标题为<b>【XX总关注】【XX机型】【XX问题】的工作群</b>，里头有<b>浩浩荡荡接近50个人</b>，人员构成大致是<b>10个主管，5个部长，10个产品经理，10个测试经理，10个公关经理，</b><b class="red">指挥</b>我和其他两名工程师干活。<b>一个诺大的寺庙里，挑水的和尚就三个</b>。
                        <br>总之，解决的<b>是<b class="red">老板</b>，不是问题。</b></li>
                </div>

                <p>OPPO内部似乎有一种<b>崇尚所谓<b class="red">“业务能力”</b></b>的氛围，如果你对项目流程很<b>熟练</b>，对和各个角色<b>扯皮得心应手</b>，能用一个又一个连环夺命电话<b>指挥其他人帮你做事</b>，能在一些有大老板在场的会议<b>侃侃而谈</b>，仿佛你就是一名<b class="red">优秀</b>的的软件工程师了。我对此实在<b>嗤之以鼻</b>，这让我想起了大洋彼岸的美国著名篮球评论员<b class="red">A史密斯</b>先生对于NBA湖人队后卫<b>威斯布鲁克</b>关于<b>“生活大于篮球”</b>观点的一段评论，大概是这样说的：“<b>威斯布鲁克先生，你是一个绝对的好人，一位积极的社区贡献者，一位热衷于公益事业的慈善家，一位伟大的丈夫和爸爸，我很尊重这些，可是<b class="red">这和你的球有他妈什么关系？</b>”</b></p>
            <h2>离开它</h2>
                <p><b class="red">消极</b>的情绪在<b>迅速蔓延</b>，一头蹿进<b class="red">死角</b>的我已经无力无心再寻找出口，只想立刻<b>翻墙逃离</b>。时间来到2021年春节假期，我做出了两个重要决定：1. <b>我必须要离职</b>； 2. <b>我必须要去微软</b>。第一个决定，上文已有解释，第二个决定，会在下一篇也是大厂之路的完结篇详聊。</p>

