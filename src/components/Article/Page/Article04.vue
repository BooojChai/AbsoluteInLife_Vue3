            <h3>闯关</h3>
                <p>当我成为<b>第一批</b>入驻南京OPPO办公楼的员工，收拾好工位开始打量新环境时，时间已接近<b>2020年春节</b>。如你所知，从那时开始，<b class="red">新冠肺炎</b>开始席卷全国，春节放假回老家的我也因为疫情拖延了两周才回到南京。回去后大部门写字楼仍处于封控状态，只能用公司邮寄到家的工作笔记本电脑远程办公，直到3月初，办公室才陆续开放进入，而彼时的我已经入职超过四个月，<b>依然没有接触到核心业务</b>，每天的工作就是竞品调研，分析，测试这些边角活，<b>这让人心慌。</b></p>

                <p>也是在这个节骨眼上，主管突然找到我，通知我因为<b>部门内部业务调整，我要从系统优化组被调去游戏优化组</b>。这当然是通知，不是商量，没有选择的余地，而要调走的人里，也当然一定会包括我这个没有接触核心业务，没有任何不可替代性的小角色。起初得知这个消息，我是<b>抗拒的，挣扎的</b>，但很快就释然了——我是一个新人，在这个组是新人，去那个组也是新人。我什么都没有，自然也<b class="red">没什么可失去</b>，未知对我来说毫不可怕，那就<b>走呗。</b></p>

                <p>去到游戏优化组，组里的主管同事基本也都是前司的同事，不同的是他们是对口业务，平移到新公司，而我<b>不能说是啥也不会，只能说是<b class="red">一窍不通</b></b>。无论从业务上，还是从人际上，融入这个组对我来说都是<b>不小的挑战</b>。换组不久后，我被安插上了一个工作项——负责OPPO <a href="https://open.oppomobile.com/new/introduction?page_name=hyperboost" target="_blank"> Hyper Boost </a>性能开放能力相关业务。这项业务虽然算不上游戏优化的核心业务，但总归是个正经活，肯定比之前做边角料强出一大截，要知道在换组之前，我还没有在公司的代码仓里提过哪怕一笔代码，无论如何，这是件<b class="red">好事</b>。</p>

                <p>Android手机的大部分底层硬件能力，比如<b>调节CPU/GPU频率、调度线程优先级</b>等影响着性能、功耗指标的关键能力，三方应用是无法直接操纵的。而所谓性能开放能力，是指手机厂商们<b>通过系统层桥接，将部分操纵硬件的能力有节制的开放给三方应用</b>们使用，意图实现应用层和系统层的双向通话，让应用在必要的场景拥有自主调度硬件的能力，提升应用性能，优化用户体验，打造更好的<b>手机系统生态</b>。性能开放能力的主要受众应用是游戏，这个业务也就归属于游戏优化组。Hyper Boost以的<a href="https://www.zhihu.com/question/286904345" target="_blank"> SDK </a>形式开放，而开发这个SDK并不费劲，无非是一些业务逻辑的组装，<b class="red">承上启下</b>，为三方应用们提供一套能力API，在SDK和使用文档完成上线后，真正的工作才刚刚开始——我需要和负责开放平台的产品运营们一起，一家一家对接应用厂商，推广我们的能力，并充当技术客服，帮助应用厂商解决他们在接入能力过程中遇到的各种问题，最终<b>实现接入，达成合作。</b></p>

                <p>既然要搞三方合作，自然也给了我一些去到应用厂商宣讲交流的机会。为了推广我们的能力，我去过<b>北京的完美世界，去过杭州的网易游戏，并两度前往深圳，分别去到腾讯的TGPA项目组和字节跳动的性能中台项目组</b>，进行了深度的交流与合作。工作总要有产出，除了执笔OPPO开放平台Hyper Boost开发文档，支援OPPO开发者大会之外，还搞出了一件不大不小的事——<b>推动OPPO、高通、网易天谕手游的三方合作</b>，在Android手机厂商里<a href="https://new.qq.com/omn/20210424/20210424A09LZ400.html" target="_blank"> 首发 </a>了VRS可变分辨率渲染技术。</p>

                <p>故事讲到这里，看似前方<b>一片光明，未来可期</b>。可现实是，并非每个踏入<b class="red">迷宫</b>的闯关者，都能幸运得找到<b>出口。</b></p>

