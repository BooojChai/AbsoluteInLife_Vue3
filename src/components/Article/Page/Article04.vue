            <h1>转.  OPPO</h1>
            <h2>加入它</h2>

                <p>我的求职之路上着不同寻常的一点——<b>从未经历过面试失败。</b></p>

                <p>促成我面试从未失手的因素里，<b class="red">运气</b>占了很大比重。有时回想一下更是觉得自己实属幸运，和大部分普通高校毕业的同行相比，职业生涯的爬升路上<b>几乎没有遇到陡坡</b>。校招的那份工作，笔试题刚好考到了计算机二级的原题。从第一家公司去三星时，因为有JY的推波助澜，做了充足的准备，有惊无险拿下。而从三星离职时，是这么个情况：</p>

                <p>三年工作经验的我，<b>没有涉足过任何一项互联网行业的<b class="red">主流技术</b></b>。以我当时的那纸简历去投递互联网大厂、手机厂商，别说拿到Offer，拿到面试机会都有些困难。而彼时的OPPO正在南京组建研发中心，正值招兵买马的初创阶段，也为此<b>放低了招聘要求</b>，<b class="red">最低门槛</b>是<b>一本院校毕业，工作年限三年以上，有目标对口企业的工作经验</b>——我就卡在这个门槛上，一分不多，一分不少。</p>

                <p>面试官是前司的部长，且在老领导那里的印象分还不错，为了组建团队，快速扩张的目标，愿意接纳我。于是，<b>零安卓开发经验的我，拿到了一纸Android系统工程师的 Offer</b>，即将迈向一条全新的跑道。当时OPPO在南京的办公楼正在装修中，还没有稳定的办公场地，包括我在内的所有南京入职OPPO的新员工，都要去<b class="red">深圳</b>总部派驻两到三个月，一来有充足的工位，二来在总部工作可以帮助新员工快速融入。<b>2019年10月30日，怀揣憧憬的我从南京禄口机场踏上了南下的旅程，即将去往那座繁华的南方一线城市——深圳。</b></p>

            <h2>服务它</h2>

            <h3>启航</h3>
                <p>从<b>深圳宝安机场</b>乘坐地铁机场线，驶出没多远便穿出隧道，<b>一望无际的海</b>透过车窗映入眼帘，夺目的波光闪烁，投射在充满亚热带特色的植被上，从眼前匆匆流过。从后海下车，各具性格的摩天大楼鳞次栉比，带头的<b>春笋大厦</b>印证着这里是中国四大一线城市之一。迈过春笋，来到深圳人才公园，<b>深圳湾的绝妙海景</b>一览无余，对岸的香港隐约可见，温暖的海风令人无法想象这是即将立冬的时节——这是我对深圳的<b class="red">初印象</b>。而我对深圳的所有美好印象，都<b>定格在这第一天。</b></p>

                <p>在我去过的城市里，如果要从中挑出一个<b>最想去第二次</b>的，应当是<b>重庆</b>，而<b>最不想去第二次</b>的，<b>非深圳莫属</b>。走在深圳的随意一条街道上放眼望去，路边的小吃店是兰州拉面，是黄焖鸡米饭，是安徽板面，是淮南牛肉汤，是海南鸡饭，罕见一家<b>粤式餐厅</b>，我猜广州应当不是这个情况。如果你留意去听路人说的话，可以听到来自全国各地，五湖四海的方言，就是难以到一句<b>粤语</b>。你想多走一走，试图寻找一些细节，提取一些元素，来描绘这座城市的特质，但就是<b>找不到</b>，只觉得这儿的年轻人<b>走路很快，仿佛慢一步就会落人一拍</b>。即使你从未期待这座城市给你<b class="red">归属感</b>，轰鸣的钢筋工地和地铁站噪杂的人群，无时无刻不在提醒你——<b>嘿，这里不是你的家。</b></p>

                <p>春笋大厦正是<b>OPPO深圳总部的所在地</b>，这里是深圳的<b>地标性建筑</b>，华润集团的总部所在地，号称每个工位的月租金成本是<b class="red">6K</b>，我就在这样的顶级办公环境里入职了。我的主管也是前司的同事，零安卓经验的我自然也没有被分配到核心业务，以<b>适应，学习为主</b>，并且尽可能协助组内有经验的同事们做一做测试的工作，总体就两个字，<b class="red">打杂</b>。当时同在深圳派驻的同事们，许多都是已经成家有娃，独身来到深圳，这也给了我们一些难得的机会，可以在周末一起撸串喝酒，谈天说地，不为繁琐的日常而担忧。这段短暂而又珍贵的同事情谊，成为了异乡生活中<b>为数不多并值得回忆的美好片段</b>。</p>

                <p>在深圳的两个月多月里，每一天都觉得时间<b>如此漫长</b>，但是行将结束时，却又觉得时间过得<b>如此之快</b>。2020年元旦的跨年之夜，我是一个人在出租屋发呆渡过的。三天之后，深圳派驻之旅结束了，回到南京的那天晚上，我在一座商场的电梯间遇到了404南京市民<b class="red">李先生</b>，诧异了一下并回过神来，对着他的背影喊了一句：“<b>逼哥</b>，<b class="red">相信未来</b>！”，李先生回过头向我抿嘴示意，随后扬长而去。 这神奇的经历似乎在告诉我——<b>嘿，欢迎你回到南京。</b></p>

